alien_color = 'green'

# If statement is True
if alien_color == 'green':
    print("You just won 5 points for shooting the green alien!")
else:
    print("You just won 10 points!")

# Change the value of the variable "alien_color"
alien_color = 'red'

# Else statement is True
if 'green' in alien_color:
    print("You just won 5 points for shooting the green alien!")
else:
    print("You just won 10 points!")