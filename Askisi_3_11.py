magic_places = ['santorini', 'norway', 'dubai', 'egypt', 'kastoria']
print(magic_places)
print(sorted(magic_places))
print(magic_places)
print(sorted(magic_places, reverse=True))
print(magic_places)
magic_places.reverse()
print(magic_places)
magic_places.reverse()
print(magic_places)
magic_places.sort()
print(magic_places)
magic_places.sort(reverse=True)
print(magic_places)
print(magic_places[5])
'''Μπερδεύω πότε μπαίνει η μεταβλητή σε παρένθεση sorted(x), και πότε μπαίνει
στην αρχή και μετά βάζουμε τελεία x.sort()'''
